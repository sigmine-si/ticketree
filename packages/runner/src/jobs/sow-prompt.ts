export interface SowFirstRoundInput {
  /** 클라이언트가 폼에 쓴 배경 — 무엇을 만들고 싶은가 */
  background: string
  /** 이미 명세가 있는 저장소인가(2차 계약) */
  hasSpecs: boolean
  /** 몇 번째 계약인가 */
  seq: number
}

export function sowFirstRoundPrompt(i: SowFirstRoundInput): string {
  const parts = [
    i.seq > 1
      ? `클라이언트가 ${i.seq}번째 과업(추가 발주)을 정의하려 한다. 대화로 과업내용서를 완성하라.`
      : '클라이언트가 새 프로젝트의 과업내용서를 만들려 한다. 대화로 과업내용서를 완성하라.',
    '',
    '## 클라이언트가 쓴 내용',
    '',
    i.background,
    '',
  ]

  if (i.hasSpecs) {
    parts.push(
      '이 저장소에는 **이미 명세가 있다.** 먼저 `specs/`를 읽어 지금까지 약속된 것을 파악하라.',
      '이번 계약은 그 위에 얹히는 것이므로, **이미 약속된 것을 이번 범위에 다시 넣지 마라.**',
      '겹치는 부분을 발견하면 그건 이번 계약의 범위가 아니라 제외 범위에 가깝다 —',
      '클라이언트에게 "그건 이미 포함되어 있어요"라고 알려라.',
    )
  } else {
    parts.push(
      '이 저장소에는 아직 명세도 코드도 없다. **맨바닥에서 시작하는 계약이다.**',
      '읽을 코드가 없으므로 탐색에 시간을 쓰지 말고, 곧바로 클라이언트에게 물어라.',
    )
  }

  parts.push(
    '',
    '여덟 항목 중 클라이언트가 이미 말한 것은 채우고, 비어 있는 것을 질문으로 만들어라.',
    '한 번에 최대 3개까지만 묻는다.',
  )
  return parts.join('\n')
}

